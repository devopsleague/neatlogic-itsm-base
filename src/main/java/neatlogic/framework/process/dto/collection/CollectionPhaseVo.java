package neatlogic.framework.process.dto.collection;

import neatlogic.framework.util.SnowflakeUtil;

import java.util.List;

public class CollectionPhaseVo {
    private Long id;
    private Long collectionId;
    private int sort;
    private String name;
    private String status;
    private Integer isActive;
    private List<CollectionPhaseTaskVo> taskList;

    public Long getId() {
        if (id == null) {
            id = SnowflakeUtil.uniqueLong();
        }
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getCollectionId() {
        return collectionId;
    }

    public void setCollectionId(Long collectionId) {
        this.collectionId = collectionId;
    }

    public int getSort() {
        return sort;
    }

    public void setSort(int sort) {
        this.sort = sort;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public Integer getIsActive() {
        return isActive;
    }

    public void setIsActive(Integer isActive) {
        this.isActive = isActive;
    }

    public List<CollectionPhaseTaskVo> getTaskList() {
        return taskList;
    }

    public void setTaskList(List<CollectionPhaseTaskVo> taskList) {
        this.taskList = taskList;
    }
}
